package com.assignment.model;

public class ProviderDetails {
	private String provider;

	private String url;

	private String authHeaderName;

	private String authHaederValue;

	private String idKey;

	private String nameKey;

	private String ownerKey;

	private String useidKey;

	private String htmlUrlKey;

	private String sshUrlKey;

	public String getUrl() {
		return url;
	}

	public void setUrl(String url) {
		this.url = url;
	}

	public String getAuthHeaderName() {
		return authHeaderName;
	}

	public void setAuthHeaderName(String authHeaderName) {
		this.authHeaderName = authHeaderName;
	}

	public String getAuthHaederValue() {
		return authHaederValue;
	}

	public void setAuthHaederValue(String authHaederValue) {
		this.authHaederValue = authHaederValue;
	}

	public String getIdKey() {
		return idKey;
	}

	public void setIdKey(String idKey) {
		this.idKey = idKey;
	}

	public String getNameKey() {
		return nameKey;
	}

	public void setNameKey(String nameKey) {
		this.nameKey = nameKey;
	}

	public String getOwnerKey() {
		return ownerKey;
	}

	public void setOwnerKey(String ownerKey) {
		this.ownerKey = ownerKey;
	}

	public String getUseidKey() {
		return useidKey;
	}

	public void setUseidKey(String useidKey) {
		this.useidKey = useidKey;
	}

	public String getHtmlUrlKey() {
		return htmlUrlKey;
	}

	public void setHtmlUrlKey(String htmlUrlKey) {
		this.htmlUrlKey = htmlUrlKey;
	}

	public String getSshUrlKey() {
		return sshUrlKey;
	}

	public void setSshUrlKey(String sshUrlKey) {
		this.sshUrlKey = sshUrlKey;
	}

	public String getProvider() {
		return provider;
	}

	public void setProvider(String provider) {
		this.provider = provider;
	}

}
